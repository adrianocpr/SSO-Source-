function skill_gift:Get(prof)
	if skill_gift[prof] then
		local result = {}
		for i,skills in ipairs(skill_gift[prof]) do
			local oneGroup = {}
			for j,skillId in ipairs(skills) do
				local oneGift = {}
				oneGift.skillId = skillId
				if gift_conf[skillId] then
					oneGift.cloth_id = gift_conf[skillId].clothid
					oneGift.enhance_level = gift_conf[skillId].enhance_level
				end
				oneGroup[j] = oneGift
			end
			result[i] = oneGroup
		end
		return result
	end
end
