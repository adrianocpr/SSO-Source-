function dlg:GetTargetName(task_id)
	local conf = global_library_config[task_id] or {}
	return conf.name or ""
end

function dlg:GetImage(task_id)
	local conf = global_library_config[task_id] or {}
	return conf.image or ""
end

function dlg:GetDesc(task_id)
	local conf = global_library_config[task_id] or {}
	return conf.desc or ""
end

function dlg:GetStar(task_id)
	local conf = global_library_config[task_id] or {}
	local star = conf.star or 1
	return star * 20
end