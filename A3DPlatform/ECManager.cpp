ECManager::ECManager(CECGameRun* pGameRun)
{
	m_pGameRun		= pGameRun;
	m_dwCurTickTime	= 0;
	m_iManagerID	= 0;
}

//	Begin recroding tick time
void ECManager::BeginTickTime()
{ 
	m_dwCurTickTime = a_GetTime(); 
}

//	End recroding tick time
void ECManager::EndTickTime()
{ 
	m_dwCurTickTime = a_GetTime() - m_dwCurTickTime; 
}
